package com.wzkcy.jdgc.bean;

public class Light {
	
	protected String name;
	protected boolean on_guard;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean isOn_guard() {
		return on_guard;
	}
	public void setOn_guard(boolean on_guard) {
		this.on_guard = on_guard;
	}
	
}
